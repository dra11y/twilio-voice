#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
#[serde(untagged)]
pub enum Voice {
    Standard(standard::Voice),
    Neural(neural::Voice),
}
impl VoicePrice for Voice {
    fn price(&self) -> f32 {
        match self {
            Voice::Standard(_) => STANDARD_VOICE_PRICE,
            Voice::Neural(_) => NEURAL_VOICE_PRICE,
        }
    }
}

